package com.mukeshkpdeveloper.dynamicviewexample;

public class LabourModel {

        String name;
        int id_;

        public LabourModel(String name, int id_) {
            this.name = name;
            this.id_ = id_;
        }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId_() {
        return id_;
    }

    public void setId_(int id_) {
        this.id_ = id_;
    }
}
